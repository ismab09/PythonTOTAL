import math

#nun pi tiene mas opciones para los expertos en numeros

resultado = math.floor(89.662)
print(resultado) #para abajo

resultado = math.ceil(89.662)
print(resultado) #para arriba

resultado = math.pi
print(resultado) #te muestra el valor del numero pi

resultado = math.log(25, 5)
print(resultado) #el segundo numero se expone por si mismo las veces necesarias para llegar al primer numero

resultado = math.tan(2565)
print(resultado) #te muestra la tangente

resultado = math.cos(2565)
print(resultado) #te muestra el coseno